class Calculator:

    def __init__(self):
        pass

    def add(self,a,b):
        return (a + b)

    def subtract(self,a,b):
        return (b-a)
    def multiply(self,a,b):
        return (a*b)
    def divide(self,a,b):
        return round(float(b/a),9)
    def square(self,a):
        return (a**2)
    def square_root(self,a):
        return (a**0.5)